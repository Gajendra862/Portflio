var play = document.getElementById("playbtn");
play.addEventListener("click", function(){
    var videoshow = document.getElementById("playvideo");
    videoshow.classList.toggle("showvideo5");
    var video1 = getElementById("hidevideo5");
    video1.videoshow = currentTime = 0 ;
});


var close = document.getElementById("close5");
close.addEventListener("click", function(){
    var videoshow = document.getElementById("playvideo");
    videoshow.classList.remove("showvideo5")
});